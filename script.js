// --- المنطق البرمجي (دون أي تغيير) ---
let currentSet = [], qIdx = 0;

function startQuiz(cat) {
    let pool = [...MASTER_DB[cat]];
    for (let i = pool.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [pool[i], pool[j]] = [pool[j], pool[i]];
    }
    currentSet = pool.slice(0, 20);
    qIdx = 0;
    document.getElementById('modal').style.display = 'flex';
    const bar = document.getElementById('penaltyBar');
    bar.innerHTML = '';
    for(let i=0; i < currentSet.length; i++) {
        let dot = document.createElement('div');
        dot.className = 'ball'; dot.id = 'ball-' + i;
        bar.appendChild(dot);
    }
    showQuestion();
}

function showQuestion() {
    const q = currentSet[qIdx];
    document.getElementById('qCounter').innerText = `د. شويدرة | السؤال ${qIdx + 1} من 20`;
    document.getElementById('qText').innerText = q.q;
    const optDiv = document.getElementById('options');
    optDiv.innerHTML = '';
    let shuffledOptions = [...q.o].sort(() => Math.random() - 0.5);
    shuffledOptions.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'opt-btn'; btn.innerText = opt;
        btn.onclick = () => {
            const isCorrect = (opt === q.a);
            document.getElementById('ball-' + qIdx).classList.add(isCorrect ? 'correct' : 'wrong');
            if(!isCorrect) alert("خطأ! الجواب: " + q.a);
            qIdx++;
            if(qIdx < currentSet.length) setTimeout(showQuestion, 200);
            else { alert("🎉 أحسنت يا بطل! أنهيت المراجعة."); closeQuiz(); }
        };
        optDiv.appendChild(btn);
    });
}
function closeQuiz() { document.getElementById('modal').style.display = 'none'; }
