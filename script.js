// المنطق البرمجي (دون تغيير في الوظائف الأساسية)
let currentSet = [], qIdx = 0;

function startQuiz(cat) {
    let allQuestions = [...MASTER_DB[cat]];
    
    // خلط البنك عشوائياً
    for (let i = allQuestions.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [allQuestions[i], allQuestions[j]] = [allQuestions[j], allQuestions[i]];
    }

    // اختيار 20 سؤالاً عشوائياً دون تكرار
    let limit = Math.min(allQuestions.length, 20); 
    currentSet = allQuestions.slice(0, limit).map(item => JSON.parse(JSON.stringify(item)));

    qIdx = 0;
    document.getElementById('modal').style.display = 'flex';
    
    const bar = document.getElementById('penaltyBar');
    bar.innerHTML = '';
    for(let i=0; i < limit; i++) {
        let dot = document.createElement('div');
        dot.className = 'ball'; dot.id = 'ball-' + i;
        bar.appendChild(dot);
    }
    showQuestion();
}

function showQuestion() {
    const q = currentSet[qIdx];
    document.getElementById('qCounter').innerText = `السؤال ${qIdx + 1} من ${currentSet.length}`;
    document.getElementById('qText').innerText = q.q;
    const optDiv = document.getElementById('options');
    optDiv.innerHTML = '';
    
    let shuffledOptions = [...q.o].sort(() => Math.random() - 0.5);

    shuffledOptions.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'opt-btn'; btn.innerText = opt;
        btn.onclick = () => {
            const isCorrect = (opt === q.a);
            document.getElementById('ball-' + qIdx).classList.add(isCorrect ? 'correct' : 'wrong');
            
            if(!isCorrect) {
                alert("إجابة تحتاج مراجعة!\nالجواب الصحيح هو: " + q.a);
            }
            
            qIdx++;
            if(qIdx < currentSet.length) {
                setTimeout(showQuestion, 300);
            } else {
                alert("تهانينا! أنهيت تحدي الـ 20 سؤالاً بنجاح.");
                closeQuiz();
            }
        };
        optDiv.appendChild(btn);
    });
}

function closeQuiz() {
    document.getElementById('modal').style.display = 'none';
}
